/*
    Calculate the volume of the cylinder: r = 5 cm, h = 9 cm
 */

class test{
    public static void main(String args[]){
      
        double radi = 5;
        double height = 9;
        double volume = 3.14*(radi*radi)* height;

        System.out.println("Total volume of cylinder "+volume);
    }
}